/* Init MPU6050 for STM32 mcu related */
void mpu6050_init(void)
{
  mpu6050_i2c_ops_t ops = {
      .i2c_write = stm32_i2c_write_reg,
      .i2c_read = stm32_i2c_read_reg};
  mpu6050_set_i2c_spec(&ops);
}

static void peripheral_init(void)
{
  /* STM32 related */
  HAL_Init();
  SystemClock_Config();
  MX_GPIO_Init();
  MX_I2C1_Init();
  MX_USART1_UART_Init();

  /* Application related*/
  mpu6050_init();
}

